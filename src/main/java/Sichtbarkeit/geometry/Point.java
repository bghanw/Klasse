package Sichtbarkeit.geometry;

public class Point {

    private static int x;
    private static int y;

    public static int getX(){
        return x;
    }
    public static void setX(int x){
        Point.x = x;
    }
    public static int getY(){
        return y;
    }
    public static void setY(int y){
        Point.y = y;
    }
}
